function Rectangle(width, height, offSetX, offSetY){
  this.width = width,
  this.height = height,
  this.offSetX = offSetX,
  this.offSetY = offSetY
}

function Circle(radius, offSetX, offSetY){
  this.radius = radius,
  this.offSetX = offSetX,
  this.offSetY = offSetY
}

function Shape(vertex){
  this.vertex = vertex
}
